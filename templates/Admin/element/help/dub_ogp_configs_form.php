<p>OGPプラグイン <a href="https://github.com/binbin4649/DubOgp" target="_blank">github</a></p>
<ul>
    <li>
        <b>Twitter ID</b> : Twitterアカウントがあったら、@の後ろを入力。
    </li>
    <li>
        <b>Twitter Card</b> : Twitter IDが入力されていれば、選択されたTwitter Cardが反映されます。
    </li>
    <li>
        <b>facebook App-ID</b> : facebookはApp-IDを入力することを推奨している。
        Facebookで開発者（デベロッパー）登録し、取得したApp-IDを記述します。
        App-IDを取得するにはFacebookにログイン後、「<a href="https://developers.facebook.com/apps/" target="_blank">すべてのアプリ – 開発者向けFacebook</a>」ページにアクセスし、「新しいアプリを追加」します。
        新しいアプリを追加し、もろもろ設定することで、App-IDが取得できます。
    </li>
    <li>
        <b>デフォルトイメージ</b> : アイキャッチがない場合に代替する画像のファイル名。
        ( app/webroot/テーマ名/img/ ) or ( app/webroot/img/ )
        デフォルトイメージはどちらかのディレクトに入れてください。
    </li>
    <li>
        <b>locale</b> : 省略すると「ja_JP」が入る。
    </li>
    <li>
        <b>locale_alternate</b> : 省略すると出力されない。
    </li>
</ul>